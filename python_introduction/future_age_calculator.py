# Prompt the user for their current age
current_age_str = input("How old are you? ")

# Convert the input age to an integer
current_age = int(current_age_str)

# Calculate age in 2050 (assuming current year is 2023, so 27 years in the future)
age_in_2050 = current_age + 27

# Print the result in the specified format
print(f"In 2050, you will be {age_in_2050} years old.")
