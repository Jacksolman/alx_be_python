# hours_to_seconds.py

# Define the variable 'hours' and assign it a value
hours = 2

# Calculate the number of seconds in the given hours
# Remember: 60 minutes in an hour, 60 seconds in a minute
# So, 60 * 60 = 3600 seconds in an hour
seconds = hours * 3600

# Print the result in the format: [hours] hour(s) is [seconds] seconds.
print(f"{hours} hour(s) is {seconds} seconds.")
